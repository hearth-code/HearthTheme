import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs'
import { pathToFileURL } from 'url'
import { buildVariantCssById, writeIfChanged } from './generate-obsidian-themes.mjs'
import { getReleaseVersion } from './release-metadata.mjs'

const APP_THEME_DIR = 'obsidian/app-theme'
const MANIFEST_PATH = `${APP_THEME_DIR}/manifest.json`
const THEME_CSS_PATH = `${APP_THEME_DIR}/theme.css`
const VERSIONS_PATH = `${APP_THEME_DIR}/versions.json`
const SCREENSHOT_PATH = `${APP_THEME_DIR}/screenshot.png`
const SUBMISSION_TEMPLATE_PATH = `${APP_THEME_DIR}/community-css-theme-entry.json`

const SCREENSHOT_SOURCE_PATH = 'public/previews/preview-contrast-v2.png'
const TARGET_SCREENSHOT_WIDTH = 512
const TARGET_SCREENSHOT_HEIGHT = 288

const THEME_NAME = 'HearthCode'
const THEME_AUTHOR = 'HearthCode'
const THEME_AUTHOR_URL = 'https://theme.hearthcode.dev'
const MIN_APP_VERSION = '1.0.0'

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'))
}

function parseRepoSlug(value) {
  if (typeof value !== 'string') return null
  const trimmed = value.trim()
  const ghMatch = trimmed.match(/github\.com[:/](.+?)(?:\.git)?$/i)
  if (!ghMatch) return null
  return ghMatch[1]
}

function semverParts(version) {
  const match = String(version).trim().match(/^(\d+)\.(\d+)\.(\d+)/)
  if (!match) return null
  return [
    Number.parseInt(match[1], 10),
    Number.parseInt(match[2], 10),
    Number.parseInt(match[3], 10),
  ]
}

function compareVersions(a, b) {
  const pa = semverParts(a) || [0, 0, 0]
  const pb = semverParts(b) || [0, 0, 0]
  for (let i = 0; i < 3; i += 1) {
    if (pa[i] !== pb[i]) return pa[i] - pb[i]
  }
  return String(a).localeCompare(String(b))
}

function stripSnippetHeader(css) {
  return css.replace(
    /^\/\* Auto-generated by scripts\/generate-obsidian-themes\.mjs - DO NOT EDIT \*\/\r?\n\/\* Variant:[^\r\n]*\*\/\r?\n\/\* Source:[^\r\n]*\*\/\r?\n/,
    ''
  )
}

function buildAppThemeCss() {
  const dark = stripSnippetHeader(buildVariantCssById('dark')).trim()
  const light = stripSnippetHeader(buildVariantCssById('light')).trim()

  return [
    '/* Auto-generated by scripts/generate-obsidian-app-theme.mjs - DO NOT EDIT */',
    `/* Theme: ${THEME_NAME} */`,
    '',
    dark,
    '',
    light,
    '',
  ].join('\n')
}

function loadThemeVersionInfo() {
  const version = getReleaseVersion()
  const extPackage = readJson('extension/package.json')

  const repoSlug = parseRepoSlug(extPackage.repository?.url) || 'hearth-code/HearthTheme'
  return { version, repoSlug }
}

function buildManifest(version) {
  return {
    name: THEME_NAME,
    version,
    minAppVersion: MIN_APP_VERSION,
    author: THEME_AUTHOR,
    authorUrl: THEME_AUTHOR_URL,
  }
}

function buildVersions(version) {
  const current = existsSync(VERSIONS_PATH) ? readJson(VERSIONS_PATH) : {}
  current[version] = MIN_APP_VERSION

  const orderedEntries = Object.entries(current).sort((a, b) => compareVersions(a[0], b[0]))
  return Object.fromEntries(orderedEntries)
}

function buildSubmissionTemplate(repoSlug) {
  return {
    name: THEME_NAME,
    author: THEME_AUTHOR,
    repo: repoSlug,
    screenshot: 'obsidian/app-theme/screenshot.png',
    modes: ['dark', 'light'],
  }
}

async function generateScreenshot() {
  if (!existsSync(SCREENSHOT_SOURCE_PATH)) {
    throw new Error(`Screenshot source not found: ${SCREENSHOT_SOURCE_PATH}`)
  }

  const sharp = (await import('sharp')).default
  const src = sharp(SCREENSHOT_SOURCE_PATH)
  const meta = await src.metadata()
  const width = Number(meta.width)
  const height = Number(meta.height)
  if (!width || !height) {
    throw new Error(`Cannot read screenshot dimensions from ${SCREENSHOT_SOURCE_PATH}`)
  }

  const sourceRatio = width / height
  const targetRatio = TARGET_SCREENSHOT_WIDTH / TARGET_SCREENSHOT_HEIGHT

  let extractWidth = width
  let extractHeight = height
  let left = 0
  let top = 0

  if (sourceRatio > targetRatio) {
    extractWidth = Math.round(height * targetRatio)
    left = Math.floor((width - extractWidth) / 2)
  } else if (sourceRatio < targetRatio) {
    extractHeight = Math.round(width / targetRatio)
    top = Math.floor((height - extractHeight) / 2)
  }

  const buffer = await sharp(SCREENSHOT_SOURCE_PATH)
    .extract({ left, top, width: extractWidth, height: extractHeight })
    .resize(TARGET_SCREENSHOT_WIDTH, TARGET_SCREENSHOT_HEIGHT, { fit: 'cover' })
    .png()
    .toBuffer()

  if (existsSync(SCREENSHOT_PATH)) {
    const prev = readFileSync(SCREENSHOT_PATH)
    if (Buffer.compare(prev, buffer) === 0) return false
  }

  writeFileSync(SCREENSHOT_PATH, buffer)
  return true
}

export async function generateObsidianAppTheme() {
  mkdirSync(APP_THEME_DIR, { recursive: true })

  const { version, repoSlug } = loadThemeVersionInfo()

  const manifest = buildManifest(version)
  const versions = buildVersions(version)
  const submissionTemplate = buildSubmissionTemplate(repoSlug)
  const css = buildAppThemeCss()

  const updates = {
    manifest: writeIfChanged(MANIFEST_PATH, `${JSON.stringify(manifest, null, 2)}\n`),
    themeCss: writeIfChanged(THEME_CSS_PATH, css),
    versions: writeIfChanged(VERSIONS_PATH, `${JSON.stringify(versions, null, 2)}\n`),
    submissionTemplate: writeIfChanged(SUBMISSION_TEMPLATE_PATH, `${JSON.stringify(submissionTemplate, null, 2)}\n`),
    screenshot: await generateScreenshot(),
  }

  console.log(`${updates.manifest ? '✓ updated' : '- unchanged'} ${MANIFEST_PATH}`)
  console.log(`${updates.themeCss ? '✓ updated' : '- unchanged'} ${THEME_CSS_PATH}`)
  console.log(`${updates.versions ? '✓ updated' : '- unchanged'} ${VERSIONS_PATH}`)
  console.log(`${updates.submissionTemplate ? '✓ updated' : '- unchanged'} ${SUBMISSION_TEMPLATE_PATH}`)
  console.log(`${updates.screenshot ? '✓ updated' : '- unchanged'} ${SCREENSHOT_PATH}`)

  return updates
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    await generateObsidianAppTheme()
  } catch (error) {
    console.error(`[FAIL] ${error.message}`)
    process.exit(1)
  }
}
