import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { spawnSync } from 'node:child_process'

const ROOT = process.cwd()
const NO_PACK = process.argv.includes('--no-pack')
const HELP = process.argv.includes('--help') || process.argv.includes('-h')
const SUMMARY_JSON_PATH = join(ROOT, 'reports', 'moss-release-gate', 'summary.json')
const SUMMARY_MD_PATH = join(ROOT, 'reports', 'moss-release-gate', 'summary.md')

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'))
}

function runStep(index, total, label, scriptName) {
  console.log(`\n[${index}/${total}] ${label}`)
  const result = spawnSync('pnpm', ['run', scriptName], {
    cwd: ROOT,
    stdio: 'inherit',
    shell: process.platform === 'win32',
  })

  if (result.error) {
    console.error(`[FAIL] ${label}: ${result.error.message}`)
    process.exit(1)
  }

  if ((result.status ?? 1) !== 0) {
    console.error(`[FAIL] ${label}`)
    process.exit(result.status ?? 1)
  }
}

function fixed(value, digits = 2) {
  if (value == null || Number.isNaN(Number(value))) return 'n/a'
  return Number(value).toFixed(digits)
}

function latestVsixPath(version) {
  const candidate = join(ROOT, 'extension', `hearth-theme-${version}.vsix`)
  return existsSync(candidate) ? candidate : null
}

function buildSummary({ packed }) {
  const reportPath = join(ROOT, 'reports', 'moss-visual-review', 'report.json')
  const contractReportPath = join(ROOT, 'reports', 'moss-release-contract-audit.json')
  const pkgPath = join(ROOT, 'extension', 'package.json')
  const report = existsSync(reportPath) ? readJson(reportPath) : null
  const contractReport = existsSync(contractReportPath) ? readJson(contractReportPath) : null
  const pkg = existsSync(pkgPath) ? readJson(pkgPath) : null
  const version = pkg?.version || 'unknown'
  const vsix = packed ? latestVsixPath(version) : null

  return {
    schemaVersion: 1,
    generatedBy: 'scripts/moss-release-gate.mjs',
    schemeId: 'moss',
    version,
    status: report?.status === 'pass' && contractReport?.status === 'pass' ? 'pass' : 'review',
    packed,
    vsixPath: vsix,
    evidence: {
      visualReport: reportPath,
      visualSummary: join(ROOT, 'reports', 'moss-visual-review', 'report.md'),
      releaseContractAudit: contractReportPath,
      releaseGateSummary: SUMMARY_MD_PATH,
    },
    visual: report
      ? {
          status: report.status,
          blockingIssues: report.issues?.length ?? 0,
          warnings: report.warnings?.length ?? 0,
          snapshotDrift: Boolean(report.snapshotDrift),
          variants: (report.variants || []).map((variant) => ({
            variantId: variant.variantId,
            clarity: variant.visualProxies?.falloutClarityScore ?? null,
            mudRisk: variant.visualProxies?.mudRisk ?? null,
            lightSurfaceDustRisk: variant.visualProxies?.lightSurfaceDustRisk ?? null,
            chromeStatus: variant.chromeMetrics?.status || null,
            snapshotPath: variant.snapshotPath || null,
          })),
        }
      : null,
    releaseContract: contractReport
      ? {
          status: contractReport.status,
          issues: contractReport.issues || [],
          warnings: contractReport.warnings || [],
        }
      : null,
  }
}

function buildSummaryMarkdown(summary) {
  const lines = [
    '# Moss Release Gate Summary',
    '',
    'Auto-generated by `scripts/moss-release-gate.mjs`.',
    '',
    `Status: ${summary.status}`,
    `Version: ${summary.version}`,
    `VSIX: ${summary.vsixPath || (summary.packed ? 'not found after packaging' : 'skipped (--no-pack)')}`,
    '',
    '## Evidence',
    '',
    `- Visual report: ${summary.evidence.visualReport}`,
    `- Visual summary: ${summary.evidence.visualSummary}`,
    `- Release contract audit: ${summary.evidence.releaseContractAudit}`,
    '',
    '## Visual Quality',
    '',
    `- Visual status: ${summary.visual?.status || 'n/a'}`,
    `- Blocking issues: ${summary.visual?.blockingIssues ?? 'n/a'}`,
    `- Warnings: ${summary.visual?.warnings ?? 'n/a'}`,
    `- Snapshot drift: ${summary.visual?.snapshotDrift ? 'yes' : 'no'}`,
    '',
    '| Variant | Clarity | Mud Risk | Light Surface Dust | Chrome | Snapshot |',
    '| --- | ---: | ---: | ---: | --- | --- |',
  ]

  for (const variant of summary.visual?.variants || []) {
    lines.push(
      `| ${variant.variantId} | ${fixed(variant.clarity)} | ${fixed(variant.mudRisk)} | ${fixed(variant.lightSurfaceDustRisk)} | ${variant.chromeStatus || 'n/a'} | ${variant.snapshotPath || 'n/a'} |`
    )
  }

  lines.push('', '## Release Contract', '')
  lines.push(`- Status: ${summary.releaseContract?.status || 'n/a'}`)
  lines.push(`- Issues: ${summary.releaseContract?.issues?.length ?? 'n/a'}`)
  lines.push(`- Warnings: ${summary.releaseContract?.warnings?.length ?? 'n/a'}`)
  lines.push('')
  return lines.join('\n')
}

function writeSummary(summary) {
  mkdirSync(dirname(SUMMARY_JSON_PATH), { recursive: true })
  writeFileSync(SUMMARY_JSON_PATH, `${JSON.stringify(summary, null, 2)}\n`)
  writeFileSync(SUMMARY_MD_PATH, buildSummaryMarkdown(summary))
}

function printSummary(summary) {
  console.log('\n[OK] Moss release gate completed.')
  console.log(`- Version: ${summary.version}`)
  console.log(`- Summary: ${SUMMARY_MD_PATH}`)
  console.log(`- Visual report: ${summary.evidence.visualReport}`)

  if (summary.visual) {
    console.log(`- Visual status: ${summary.visual.status}`)
    console.log(`- Blocking issues: ${summary.visual.blockingIssues}`)
    console.log(`- Warnings: ${summary.visual.warnings}`)
    console.log(`- Snapshot drift: ${summary.visual.snapshotDrift ? 'yes' : 'no'}`)

    for (const variant of summary.visual.variants || []) {
      console.log(`- ${variant.variantId}: clarity ${fixed(variant.clarity)}, mud ${fixed(variant.mudRisk)}, dust ${fixed(variant.lightSurfaceDustRisk)}, chrome ${variant.chromeStatus || 'n/a'}`)
    }
  }

  if (summary.packed) {
    console.log(`- VSIX: ${summary.vsixPath || 'not found after packaging'}`)
  } else {
    console.log('- VSIX: skipped (--no-pack)')
  }
}

function usage() {
  console.log(`Usage:
  pnpm run gate:moss
  pnpm run gate:moss -- --no-pack

Flow:
  1) sync generated outputs
  2) check generated drift
  3) check preview drift
  4) run theme audit
  5) run color contract audit
  6) run Moss visual CI review
  7) validate Moss release contract against the visual report
  8) run content/copy/claims/release checks
  9) run tests
  10) build site
  11) test built site
  12) package VSIX unless --no-pack is provided
  13) write reports/moss-release-gate/summary.md`)
}

if (HELP) {
  usage()
  process.exit(0)
}

const steps = [
  ['Sync outputs', 'sync'],
  ['Check sync drift', 'check:sync'],
  ['Check preview drift', 'check:preview'],
  ['Theme audit', 'audit:theme'],
  ['Color contract audit', 'audit:color-contract'],
  ['Moss visual CI review', 'audit:moss-visual'],
  ['Moss release contract audit', 'audit:moss-release-contract'],
  ['Content sync audit', 'audit:copy'],
  ['Claims audit', 'audit:claims'],
  ['Release consistency audit', 'audit:release'],
  ['Unit tests', 'test'],
  ['Build site', 'build'],
  ['Built site smoke test', 'test:site'],
]

if (!NO_PACK) {
  steps.push(['Package extension VSIX', 'pack:ext'])
}

for (let index = 0; index < steps.length; index += 1) {
  const [label, scriptName] = steps[index]
  runStep(index + 1, steps.length, label, scriptName)
}

const summary = buildSummary({ packed: !NO_PACK })
writeSummary(summary)
printSummary(summary)
